void toggle_num_layer(void);

void toggle_num_layer() {
  if (IS_LAYER_ON(NUM)) {
    // turn off the layer
    layer_off(NUM);

    // send f18
    SEND_STRING(SS_TAP(X_F18));
  } else {
    // turn layer on
    layer_on(NUM);

    // send f17
    SEND_STRING(SS_TAP(X_F17));
  }
}


// SHIFT + BACKSPACE = DELETE
// Initialize variable holding the binary
// representation of active modifiers.
uint8_t mod_state;
bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    mod_state = get_mods();
    switch (keycode) {

      case T_NUM:
        if (record->event.pressed) toggle_num_layer();

        return false;

    case LGUI_T(KC_BSPC):
        {
        // Initialize a boolean variable that keeps track
        // of the delete key status: registered or not?
        static bool delkey_registered;
        if (record->event.pressed) {
            // Detect the activation of either shift keys
            if (mod_state & MOD_MASK_SHIFT) {
                // First temporarily canceling both shifts so that
                // shift isn't applied to the KC_DEL keycode
                del_mods(MOD_MASK_SHIFT);
                register_code(KC_DEL);
                // Update the boolean variable to reflect the status of KC_DEL
                delkey_registered = true;
                // Reapplying modifier state so that the held shift key(s)
                // still work even after having tapped the Backspace/Delete key.
                set_mods(mod_state);
                return false;
            }
        } else { // on release of KC_BSPC
            // In case KC_DEL is still being sent even after the release of KC_BSPC
            if (delkey_registered) {
                unregister_code(KC_DEL);
                delkey_registered = false;
                return false;
            }
        }
        // Let QMK process the KC_BSPC keycode as usual outside of shift
        return true;
    }

    // Prevent accidental activation of Layer 6
    case HM_N:
        if (record->event.pressed) {
            if (IS_LAYER_ON(4)) {
                tap_code(KC_E);
                tap_code(KC_N);
                return false;
            }
        }
        return true;

    // Prevent accidenal activation of Left Control
    case HM_T:
        if (record->event.pressed) {
            if (IS_LAYER_ON(3)) {
                tap_code(KC_S);
                tap_code(KC_T);
                return false;
            }
        }
        return true;

    case HM_S:
        if (record->event.pressed) {
            // Prevent activation of Layer 1 on outward roll
            if (IS_LAYER_ON(1)) {
                layer_off(1);
                tap_code(KC_T);
                tap_code(KC_S);
                return false;
            }
            // Prevent activaction of Layer 3 on inward roll
            if (IS_LAYER_ON(5)) {
                layer_off(5);
                tap_code(KC_R);
                tap_code(KC_S);
                return false;
            }
        }
        return true;

    // Prevent activation of Layer 2 on outward roll
    case HM_E:
        if (record->event.pressed) {
            if (IS_LAYER_ON(2)) {
                layer_off(2);
                tap_code(KC_N);
                tap_code(KC_E);
                return false;
            }

            // Prevent activaction of Layer 4 on inward roll
            if (IS_LAYER_ON(6)) {
                layer_off(6);
                tap_code(KC_I);
                tap_code(KC_E);
                return false;
            }
        }
        return true;

    // Prevent activaction of Layer 3 on outward roll
    case HM_A:
        if (record->event.pressed) {
            if (IS_LAYER_ON(5)) {
                layer_off(5);
                tap_code(KC_R);
                tap_code(KC_A);
                return false;
            }
        }
        return true;

    // Prevent activaction of Layer 4 on outward roll
    case HM_O:
        if (record->event.pressed) {
            if (IS_LAYER_ON(6)) {
                layer_off(6);
                tap_code(KC_I);
                tap_code(KC_O);
                return false;
            }
        }
        return true;

    case HM_I:
        if (record->event.pressed) {
            // Prevent activaction of Layer 2 on jump from n to i
            if (IS_LAYER_ON(2)) {
                layer_off(2);
                tap_code(KC_N);
                tap_code(KC_I);
                return false;
            }
            // Prevent activaction of Right Control
            if (IS_LAYER_ON(4)) {
                tap_code(KC_E);
                tap_code(KC_I);
                return false;
            }
        }
        return true;

    }
    return true;
};

